-- All the options setting here

vim.opt.number = true 		-- show line numbers

-- making line numbers default
vim.o.number = true

vim.opt.relativenumber = true 		-- relative numbers for motions
vim.opt.mouse = "a"		-- enable mouse
vim.opt.termguicolors = true 	-- full color support in terminal
vim.opt.ignorecase = true	-- ignores case for search
vim.opt.smartcase = true	-- enabling smartcase

vim.opt.clipboard = "unnamedplus"

vim.opt.tabstop = 4
vim.opt.softtabstop = -1	--matches automatically to shiftwidth
vim.opt.shiftwidth = 4
vim.opt.expandtab = true	-- use spaces, not \t

